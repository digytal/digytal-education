package digytal.util.desktop.ss;

public class SSRelatorioException extends Exception {
    public SSRelatorioException() {
        super();
    }
    
    public SSRelatorioException(Exception e) {
        super(e);
    }
    
    public SSRelatorioException(String msg) {
        super(msg);
    }
    
    public SSRelatorioException(String message, Throwable cause) {
        super(message, cause);
    }
}
